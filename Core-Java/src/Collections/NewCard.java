package Collections;

public class NewCard {
	private String symbol;
	private int number;
	
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int num) {
		this.number = num;
	}
}
